/*
TYPES OF FUNCTIONS (4)

*/

// 1. NO PARAMETERS NO RETURN
function show() {
    console.log("hello")
}

// 2. Parameters with no return
function add(x, y) {
    console.log(x + y)
}

// 3. No parameters but return
function giveName() {
    const x = "mehedy"
    return x;
}

// 4. Parameters with return
function giveDouble(x) {
    return x * 2;
}

